# for loop = a statement that will execute it's block of code,
print("\nLoop 1: ")
for i in range(5):
    print(i)

print("\nLoop 2: ")
for i in range(2,5):
    print(i)

 # Nested loop

print("\nLoop 3: ")
for i in range(2):
    for j in range(3):
        print(i, j , end="") # end="" removes the new line, remove it to get rows and columns